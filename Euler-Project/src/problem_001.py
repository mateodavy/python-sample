import os
import sys

from Log import Log


# ---------------------------------------------------------------------------------------------------------
# defines
# ---------------------------------------------------------------------------------------------------------

APPLICATION_NAME = "001"


# ---------------------------------------------------------------------------------------------------------
# utilities
# ---------------------------------------------------------------------------------------------------------



# ---------------------------------------------------------------------------------------------------------
# main
# ---------------------------------------------------------------------------------------------------------

def main():
	
	# set application name
	Log.setApplicationName(APPLICATION_NAME)
	
	# start
	Log.trace("starting...")

	# compute sum
	total = 0
	for number in range(1000):
		if ((number % 3) == 0) or \
		   ((number % 5) == 0):
			total += number

	# print result
	Log.trace("total: {0}".format(total))
	
	# end
	Log.trace("done!")


# ---------------------------------------------------------------------------------------------------------
# main
# ---------------------------------------------------------------------------------------------------------

if __name__ == "__main__":
	main()
